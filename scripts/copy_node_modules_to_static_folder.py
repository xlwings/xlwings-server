import os
import shutil
from pathlib import Path

this_dir = Path(__file__).resolve().parent

node_modules_dir = this_dir.parent / "node_modules"
vendor_dir = this_dir.parent / "app" / "static" / "vendor"

node_modules_files = [
    "socket.io/client-dist/socket.io.min.js",
    "socket.io/LICENSE",
    "@alpinejs/csp/dist/cdn.min.js",
    "htmx.org/dist/htmx.min.js",
    "htmx.org/dist/ext/head-support.js",
    "htmx.org/dist/ext/loading-states.js",
    "htmx.org/LICENSE",
    "bootstrap/dist/js/bootstrap.bundle.min.js",
    "bootstrap/LICENSE",
    "@microsoft/office-js/dist",
    "@microsoft/office-js/LICENSE.md",
    "xlwings/xlwingsjs/dist/xlwings.min.js",
]

package_names = set(
    [node_module_file.split("/")[0] for node_module_file in node_modules_files]
)
for package_name in package_names:
    shutil.rmtree(vendor_dir / package_name, ignore_errors=True)
    (vendor_dir / package_name).mkdir(exist_ok=True)

for file_or_dir in node_modules_files:
    package_name, internal_path = file_or_dir.split("/", 1)
    source_path = Path(node_modules_dir / file_or_dir)
    if source_path.is_file():
        destination_dir = (vendor_dir / package_name / internal_path).parent
        destination_path = vendor_dir / package_name / internal_path
        os.makedirs(destination_dir, exist_ok=True)
        shutil.copy(source_path, destination_path)
        if source_path.suffix == ".js" and source_path.with_suffix(".js.map").is_file():
            shutil.copy(
                source_path.with_suffix(".js.map"),
                destination_path.with_suffix(".js.map"),
            )
    else:
        shutil.copytree(source_path, vendor_dir / file_or_dir)

